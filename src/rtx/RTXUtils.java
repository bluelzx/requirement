package rtx;

public class RTXUtils {

	
	/** rxt服务器地址 */
	private String serverUrl;
	
	/** 端口 */
	private String port;

	public String getServerUrl() {
		return serverUrl;
	}

	public void setServerUrl(String serverUrl) {
		this.serverUrl = serverUrl;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}

}
